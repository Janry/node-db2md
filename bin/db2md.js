#!/usr/bin/env node

var program = require("commander"),
    main,
    Main = require(__dirname + "/../lib/main");

program
    .version(require(__dirname + "/../package.json").version)
    .option(
        "-c, --config [path]",
        "path to configuration file. Default: ./db2md.json",
        process.cwd() + "/db2md.json"
    )
    .option("-G, --nogeneratedby", "no write 'Generated by' block")
    .parse(process.argv);

main = new Main(require(program.config));

main.run(function (errors, data) {
    console.log(data);

    if (program.nogeneratedby !== true) {
        console.log("\n<small>Generated by [db2md](https://github.com/index0h/node-db2md)</small>");
    }
});